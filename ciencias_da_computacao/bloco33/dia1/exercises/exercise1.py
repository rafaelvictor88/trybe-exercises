def show_me_the_bigger_number(number1, number2):
    if number1 > number2:
        return print(number1)
    elif number2 > number1:
        return print(number2)
    else:
        return print('Not exist bigger number')


show_me_the_bigger_number(10, 2)
show_me_the_bigger_number(8, 15)
show_me_the_bigger_number(5, 5)
